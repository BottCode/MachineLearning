class Perceptron:

    def __init__(self, weights, learning_rate):
        self._weights = weights
        self._learning_rate = learning_rate
    
    def setWeights(w):
        self._weights = w
    
    def setLearningRate(learning_rate):
        self._learning_rate = learning_rate
    
    def getWeights():
        return self._weights
    
    def getLearningRate():
        return self._learning_rate
    
